public class salaBL {
    int id_sala;
    String nombre_sala, escuela_sala, ubicacion_sala, descripcion_sala, piso_sala;

    public int getId_sala() {
        return id_sala;
    }

    public void setId_sala(int id_sala) {
        this.id_sala = id_sala;
    }

    public String getNombre_sala() {
        return nombre_sala;
    }

    public void setNombre_sala(String nombre_sala) {
        this.nombre_sala = nombre_sala;
    }

    public String getEscuela_sala() {
        return escuela_sala;
    }

    public void setEscuela_sala(String escuela_sala) {
        this.escuela_sala = escuela_sala;
    }

    public String getUbicacion_sala() {
        return ubicacion_sala;
    }

    public void setUbicacion_sala(String ubicacion_sala) {
        this.ubicacion_sala = ubicacion_sala;
    }

    public String getDescripcion_sala() {
        return descripcion_sala;
    }

    public void setDescripcion_sala(String descripcion_sala) {
        this.descripcion_sala = descripcion_sala;
    }

    public String getPiso_sala() {
        return piso_sala;
    }

    public void setPiso_sala(String piso_sala) {
        this.piso_sala = piso_sala;
    }
}
