    private void jButtonAgregar_SalaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAgregar_SalaActionPerformed
        // TODO add your handling code here:
        conexion objConexion = new conexion();
        
        salaBL oSala = recuperarDatosGUI();
        
        String strSentenciaInsert = String.format("INSERT INTO Sala (id_sala, nombre_sala, escuela_sala, ubicacion_sala, descripcion_sala, piso_sala)"
                + "VALUES (null, '%s', '%s', '%s', '%s', '%s')", oSala.getNombre_sala(), oSala.getEscuela_sala(), oSala.getUbicacion_sala(), oSala.getDescripcion_sala(), oSala.getPiso_sala());
        
        objConexion.ejecutarSentenciaSQL(strSentenciaInsert);
        this.mostrarDatos();
        this.limpiar();
    }//GEN-LAST:event_jButtonAgregar_SalaActionPerformed

    private void jTableSalaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableSalaMouseClicked
        // TODO add your handling code here:
        if(evt.getClickCount () == 1){
            JTable receptor = (JTable)evt.getSource();
            jTextFieldId_Sala.setText(receptor.getModel().getValueAt(receptor.getSelectedRow(),0).toString());
            jTextFieldNombre_Sala.setText(receptor.getModel().getValueAt(receptor.getSelectedRow(),1).toString());
            jTextFieldEscuela_Sala.setText(receptor.getModel().getValueAt(receptor.getSelectedRow(),2).toString());
            jTextFieldUbicacion_Sala.setText(receptor.getModel().getValueAt(receptor.getSelectedRow(),3).toString());
            jTextFieldDescripcion_Sala.setText(receptor.getModel().getValueAt(receptor.getSelectedRow(),4).toString());
            jTextFieldPiso_Sala.setText(receptor.getModel().getValueAt(receptor.getSelectedRow(),5).toString());
        }
        jButtonAgregar_Sala.setEnabled(false);
        jButtonModificar_Sala.setEnabled(true);
        jButtonEliminar_Sala.setEnabled(true);
    }//GEN-LAST:event_jTableSalaMouseClicked

