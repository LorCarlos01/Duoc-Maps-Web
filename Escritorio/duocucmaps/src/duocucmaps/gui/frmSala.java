    public void mostrarDatos() {
        while(modelo.getRowCount()>0){
            modelo.removeRow(0);
        }
        conexion objConexion = new conexion();
        
        try {
            ResultSet resultado = objConexion.consultarRegistros("SELECT * FROM Sala");
            while (resultado.next()) {
                System.out.println(resultado.getString("id_sala"));
                System.out.println(resultado.getString("nombre_sala"));
                System.out.println(resultado.getString("escuela_sala"));
                System.out.println(resultado.getString("ubicacion_sala"));
                System.out.println(resultado.getString("descripcion_sala"));
                System.out.println(resultado.getString("piso_sala"));
                
                Object[] oSala = {resultado.getString("id_sala"), resultado.getString("nombre_sala"), resultado.getString("escuela_sala"), resultado.getString("ubicacion_sala"), resultado.getString("descripcion_sala"), resultado.getString("piso_sala")};
                
                modelo.addRow(oSala);
            }
        }
        catch (Exception e){
            System.out.println(e);
        }
    }
    
    public salaBL recuperarDatosGUI() {
        salaBL oSala = new salaBL();
        int id_sala = (jTextFieldId_Sala.getText().isEmpty()) ?0 : Integer.parseInt(jTextFieldId_Sala.getText());
        
        oSala.setId_sala(id_sala);
        oSala.setNombre_sala(jTextFieldNombre_Sala.getText());
        oSala.setEscuela_sala(jTextFieldEscuela_Sala.getText());
        oSala.setUbicacion_sala(jTextFieldUbicacion_Sala.getText());
        oSala.setDescripcion_sala(jTextFieldDescripcion_Sala.getText());
        oSala.setPiso_sala(jTextFieldPiso_Sala.getText());
        
        return oSala;
        
    }
